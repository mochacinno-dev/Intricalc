import math

def square(a):
    square = math.sqrt(a)
    return square

def power(a, b):
    power = math.pow(a, b)
    return power

def cosene(a):
    cosene = math.cos(a)
    return cosene

def sin(a):
    sine = math.sin(a)
    return sine

def tangent(a):
    tangent = math.tan(a)
    return tangent

def arccos(a):
    arccos = math.acos(a)
    return arccos

def arcotan(a):
    arcotang = math.atan(a)
    return arcotang

def arcosin(a):
    arcosine = math.asin(a)
    return arcosine
    